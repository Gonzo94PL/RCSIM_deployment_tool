def crsf_crc8(data: bytes) -> int:
    crc = 0
    for b in data:
        crc = _CRC8_TAB[crc ^ b]
    return crc

class CRSFOutput(BaseOutput):
    def __init__(self, rc_state: RCState, port_path: str = "/dev/ttyAMA0") -> None:
        # Standardowa prędkość TBS Crossfire to 420000 bps
        # CRSF naturalnie pracuje z prędkościami 150 Hz lub nawet 250 Hz (używamy 0.0066s = ~150 Hz)
        super().__init__(rc_state, port_path, baudrate=420000, interval=0.0066)

    def serialize_channels(self, channels: list[int]) -> bytes:
        """
        Pakuje wartości 16 kanałów µs [1000..2000] na 11-bitowy format cyfrowy CRSF [172..1811].
        Suma bajtów ramki:
            [0xEE] [0x18] [0x16] [22 bajty spakowanych osi] [CRC8]
            Razem: 26 bajtów.
        """
        # 1. Konwersja czasu us [1000, 2000] na 11-bit zakres CRSF [172, 1811]
        crsf_vals = []
        for val in channels[:16]:
            # Konwersja odwrotna z: channels[i] = (channels[i] - 992) / 1.6 + 1500
            # dająca: crsf_val = (us_val - 1500) * 1.6 + 992
            crsf_val = int(round((val - 1500.0) * 1.6 + 992.0))
            crsf_val = max(172, min(1811, crsf_val))
            crsf_vals.append(crsf_val)

        # Uzupełnij do 16 kanałów jeśli brakuje
        while len(crsf_vals) < 16:
            crsf_vals.append(992)

        # 2. Pakowanie 16 kanałów po 11-bitów do tablicy 22 bajtów
        packed = bytearray(22)
        bit_bucket = 0
        bits_in_bucket = 0
        byte_idx = 0

        for val in crsf_vals:
            bit_bucket |= (val << bits_in_bucket)
            bits_in_bucket += 11
            while bits_in_bucket >= 8:
                if byte_idx < 22:
                    packed[byte_idx] = bit_bucket & 0xFF
                    byte_idx += 1
                bit_bucket >>= 8
                bits_in_bucket -= 8

        # Zapis końcówki bitowej (jeśli została, choć 16 * 11 = 176 bitów = dokładnie 22 bajty)
        if bits_in_bucket > 0 and byte_idx < 22:
            packed[byte_idx] = bit_bucket & 0xFF

        # 3. Formowanie kompletnego pakietu
        # Adres docelowy: 0xEE, długość pliku: 24 (typ 0x16 + 22 bajty danych + 1 bajt CRC)
        header = bytes([0xEE, 24, 0x16])
        payload = bytes(packed)
        
        # Obliczenie sumy kontrolnej nad Typem (0x16) i Payloadem
        crc = crsf_crc8(bytes([0x16]) + payload)
        
        return header[0:2] + header[2:3] + payload + bytes([crc])
